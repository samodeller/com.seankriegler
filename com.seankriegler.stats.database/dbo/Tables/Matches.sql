CREATE TABLE [dbo].[Matches](
[Id] BIGINT IDENTITY(1,1) NOT NULL PRIMARY KEY,
[StadiumId] UNIQUEIDENTIFIER NOT NULL,
[SeasonId] INTEGER NOT NULL,
[KickOffTime] DATETIME NOT NULL,
[MatchTypeId] INTEGER NOT NULL,
[Periods] INTEGER NOT NULL,
[PeriodLength] INTEGER NOT NULL,
[CoachId] UNIQUEIDENTIFIER NULL,
[OpponentCoachId] UNIQUEIDENTIFIER NULL,
[ClubId] UNIQUEIDENTIFIER NOT NULL,
[IsExtraTime] BIT NOT NULL DEFAULT 0,
[ExtraTime] INTEGER NOT NULL DEFAULT 0,
[isCup] BIT NOT NULL DEFAULT 0,
[IsAway] BIT NOT NULL DEFAULT 0,
[IsComplete] BIT NOT NULL DEFAULT 0,
[MOM_PlayerId] UNIQUEIDENTIFIER NULL,
[Notes] NVARCHAR(MAX) NULL,
[DateCreated] DATETIME NOT NULL DEFAULT GETDATE())
GO